"""
Comparison operators in Python :-
Operator        Name                            Example
---------------------------------------------------------
==              Equal                           x== y
!=              Not equal                       x!=y
>               Greater than                    x>y
<               Less than                       x<y
>=              Greater than or equal to        x>=y
<=              Less than or equal to           x<=y
---------------------------------------------------------
"""
# Example :-
x = 15
y = 20
z = x

print(x == y)
print(z != y)
print(x > y)
print(z < y)
print(x >= z)
print(y <= z)

